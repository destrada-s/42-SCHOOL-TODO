int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb < 0)
		return (0);
	while (a <= nb)
	{
		if (a * a == nb)
			return (a);
		if (a > 46340)
			return (0);
	a++;
	}
	return (0);
}
